function compare(ino1, ino2) {
    if (ino1 > ino2) {
        console.log("first is greater than second");
    }
    else if (ino1 < ino2) {
        console.log("first is less than second");
    }
    else if (ino1 == ino2) {
        console.log("Both the number are equal");
    }
}
compare(100, 200);
