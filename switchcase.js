function days(ino1) {
    switch (ino1) {
        case 1:
            console.log("Monday");
            break;
        case 2:
            console.log("Tue");
            break;
        case 3:
            console.log("Wed");
            break;
        case 4:
            console.log("thu");
            break;
        case 5:
            console.log("fri");
            break;
        case 6:
            console.log("sat");
            break;
        case 7:
            console.log("sun");
            break;
        default:
            console.log("wrong input");
    }
}
days(3);
